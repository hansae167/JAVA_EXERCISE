package chapter05;

public class GradeIfElseTest {

	public static void main(String[] args) {
		int st1Sc = 95;//student1 Score
		int st2Sc = 88;//student2 Score
		
		if(st1Sc >= 90) {
			System.out.println("학생1 : A학점입니다.");
		}
		else
			System.out.println("학생1 : 퇴학입니다.");
		
		if(st2Sc >= 90) {
			System.out.println("학생2 : A학점입니다.");
		}
		else
			System.out.println("학생2 : 퇴학입니다.");

	}

}
