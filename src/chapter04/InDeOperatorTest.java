package chapter04;

public class InDeOperatorTest {

	public static void main(String[] args) {
		int num = 10;
		
		System.out.println(++num);//더한뒤 출력-전치형
		System.out.println(num++);//출력뒤 더함-후치형
		System.out.println(--num);//뺀뒤에 출력
		System.out.println(num--);//출력후에 뺌
		System.out.println(num);//출력
		//?
	}

}
